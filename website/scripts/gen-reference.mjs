#!/usr/bin/env node
// @ts-check
/**
 * gen-reference.mjs — derive the ENTIRE browserlane reference from the `bl`
 * binary and emit MDX into content/docs/{cli-reference,mcp-reference}.
 *
 * Strategy
 *  - CLI: run `bl <cmd> --help` and recurse into "Available Commands".
 *    Most commands use Cobra help (Usage:/Flags:/Global Flags:/Examples:);
 *    `add-mcp` uses clap help (Usage: <one line>/Arguments:/Options:). Both
 *    are parsed.
 *  - MCP: spawn `bl mcp` (stdio JSON-RPC 2.0), initialize, then tools/list.
 *
 * Output is fully regenerated on every run (idempotent). The CLI/MCP reference
 * directories are wiped (except this generator never touches anything else).
 *
 * Run: `pnpm gen:reference` (also wired into `prebuild`).
 */

import { createHash } from 'node:crypto';
import { execFileSync, spawn } from 'node:child_process';
import {
  chmodSync,
  copyFileSync,
  existsSync,
  mkdirSync,
  readdirSync,
  rmSync,
  writeFileSync,
} from 'node:fs';
import { arch as osArch, platform as osPlatform } from 'node:os';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const WEBSITE_ROOT = resolve(__dirname, '..');
const CONTENT = join(WEBSITE_ROOT, 'content', 'docs');
const CLI_DIR = join(CONTENT, 'cli-reference');
const MCP_DIR = join(CONTENT, 'mcp-reference');

const AUTOGEN_HEADER =
  '{/* AUTO-GENERATED by scripts/gen-reference.mjs — do not edit by hand; run `pnpm gen:reference` */}';

// ---------------------------------------------------------------------------
// A. Binary resolution
//      BL_BIN > ../target/release/bl > `bl` on PATH > fetch a pinned release
//
// The release-download fallback is what makes the docs build on Vercel, where
// there is no local `bl`. It mirrors install.sh: detect OS/arch -> target
// triple, download bl-<version>-<target>.tar.gz from the GitHub release,
// verify it against SHA256SUMS, extract, cache, and chmod +x. The version is
// the latest published release by default (override with BL_VERSION).
// ---------------------------------------------------------------------------

const REPO = 'browserlane/browserlane';
// Downloaded binaries are cached here (gitignored) so repeat builds are fast.
const CACHE_DIR = join(WEBSITE_ROOT, '.bl-cache');

/** Detect the release target triple for the current host (cf. install.sh). */
function detectTarget() {
  const p = osPlatform();
  const a = osArch();
  if (p === 'darwin') {
    if (a === 'arm64') return 'aarch64-apple-darwin';
    if (a === 'x64') return 'x86_64-apple-darwin';
    throw new Error(`unsupported macOS arch: ${a}`);
  }
  if (p === 'linux') {
    if (a === 'x64') return 'x86_64-unknown-linux-gnu';
    throw new Error(
      `no prebuilt Linux binary for arch "${a}" — build \`bl\` from source`,
    );
  }
  throw new Error(
    `unsupported platform "${p}" for the release-download fallback — ` +
      'build `bl` from source or set BL_BIN',
  );
}

/** Fetch a URL into a Buffer (Node built-in fetch). Throws on non-2xx. */
async function fetchBuffer(url) {
  const res = await fetch(url, {
    redirect: 'follow',
    headers: { 'User-Agent': 'browserlane-docs-gen' },
  });
  if (!res.ok) {
    throw new Error(`HTTP ${res.status} ${res.statusText} for ${url}`);
  }
  return Buffer.from(await res.arrayBuffer());
}

/**
 * Resolve which release tag to download. Pinned via BL_VERSION; when unset (or
 * "latest"), query the GitHub API for the newest published release so the docs
 * always build against the latest `bl` with no manual version bumping.
 */
async function resolveReleaseTag() {
  const pin = process.env.BL_VERSION;
  if (pin && pin !== 'latest') return pin;
  try {
    const body = (
      await fetchBuffer(`https://api.github.com/repos/${REPO}/releases/latest`)
    ).toString('utf8');
    const tag = JSON.parse(body).tag_name;
    if (!tag) throw new Error('no tag_name in the releases/latest response');
    console.log(`[gen-reference] latest release resolved: ${tag}`);
    return tag;
  } catch (e) {
    throw new Error(
      `could not resolve the latest release tag from the GitHub API ` +
        `(${e.message}); pin one explicitly with BL_VERSION=vX.Y.Z`,
    );
  }
}

/**
 * Download the resolved `bl` release for the current platform, verify its
 * checksum against the release SHA256SUMS, extract it, cache it under
 * .bl-cache/<version>/<target>/bl, chmod +x, and return its path.
 * Caches: a present, executable cached binary short-circuits the download.
 */
async function downloadReleaseBinary() {
  const target = detectTarget();
  const version = await resolveReleaseTag();
  const asset = `bl-${version}-${target}.tar.gz`;
  const base = `https://github.com/${REPO}/releases/download/${version}`;
  const outDir = join(CACHE_DIR, version, target);
  const outBin = join(outDir, 'bl');

  if (existsSync(outBin)) {
    console.log(`[gen-reference] using cached release binary: ${outBin}`);
    return outBin;
  }

  console.log(
    `[gen-reference] no local binary — downloading ${asset} (${version}, ${target})`,
  );
  mkdirSync(outDir, { recursive: true });
  const tgzPath = join(outDir, asset);

  // 1. Download the archive.
  let archive;
  try {
    archive = await fetchBuffer(`${base}/${asset}`);
  } catch (e) {
    throw new Error(
      `download failed for ${asset}\n  url: ${base}/${asset}\n  cause: ${e.message}\n` +
        `  -> is ${version} released for ${target}? ` +
        `set BL_VERSION to a valid tag, or BL_BIN to a local binary.`,
    );
  }
  writeFileSync(tgzPath, archive);

  // 2. Verify the checksum against SHA256SUMS (loud failure on mismatch;
  //    skip only if the release genuinely has no SHA256SUMS, like install.sh).
  try {
    const sums = (await fetchBuffer(`${base}/SHA256SUMS`)).toString('utf8');
    const want = sums
      .split('\n')
      .map((l) => l.trim().split(/\s+/))
      .find((cols) => cols[1] === asset)?.[0];
    if (want) {
      const got = createHash('sha256').update(archive).digest('hex');
      if (want !== got) {
        throw new Error(
          `checksum mismatch for ${asset}\n  want: ${want}\n  got:  ${got}\n` +
            '  -> refusing to use a corrupt/tampered download.',
        );
      }
      console.log('[gen-reference] checksum ok');
    } else {
      console.warn(
        `[gen-reference] note: ${asset} not listed in SHA256SUMS — skipping checksum`,
      );
    }
  } catch (e) {
    // A checksum *mismatch* must abort; a missing SHA256SUMS file should not.
    if (/checksum mismatch/.test(e.message)) throw e;
    console.warn(
      `[gen-reference] note: could not verify checksum (${e.message}) — continuing`,
    );
  }

  // 3. Extract and locate `bl`. The archive nests it under a top-level dir
  //    (bl-<version>-<target>/bl), so normalize it to the canonical cache path
  //    `outBin` — that's what the cache short-circuit at the top checks.
  try {
    execFileSync('tar', ['-xzf', tgzPath, '-C', outDir], { stdio: 'ignore' });
  } catch (e) {
    throw new Error(`failed to extract ${asset}: ${e.message}`);
  }
  const extracted = existsSync(outBin) ? outBin : findFileNamed(outDir, 'bl');
  if (!extracted) {
    throw new Error(`could not find \`bl\` inside ${asset} after extraction`);
  }
  if (resolve(extracted) !== resolve(outBin)) {
    copyFileSync(extracted, outBin);
  }
  chmodSync(outBin, 0o755);
  console.log(`[gen-reference] release binary ready: ${outBin}`);
  return outBin;
}

/** Shallow-recursive search for a file named `name` under `dir`. */
function findFileNamed(dir, name) {
  for (const ent of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, ent.name);
    if (ent.isFile() && ent.name === name) return full;
    if (ent.isDirectory()) {
      const hit = findFileNamed(full, name);
      if (hit) return hit;
    }
  }
  return null;
}

async function resolveBin() {
  const tried = [];
  if (process.env.BL_BIN) {
    const p = resolve(process.env.BL_BIN);
    tried.push(`BL_BIN=${p}`);
    if (existsSync(p)) return p;
  }
  const local = resolve(WEBSITE_ROOT, '..', 'target', 'release', 'bl');
  tried.push(local);
  if (existsSync(local)) return local;

  // `bl` on PATH
  tried.push('bl (on PATH)');
  try {
    execFileSync('bl', ['--version'], { stdio: 'ignore' });
    return 'bl';
  } catch {
    /* fall through to the release-download fallback */
  }

  // Final fallback: download a pinned release for this platform. This is the
  // path Vercel's build takes (no local `bl`).
  tried.push(`release download (${REPO}@${process.env.BL_VERSION || 'latest'})`);
  try {
    return await downloadReleaseBinary();
  } catch (e) {
    console.error(
      '\n[gen-reference] FATAL: could not obtain the `bl` binary.\n' +
        'Tried, in order:\n' +
        tried.map((t) => `  - ${t}`).join('\n') +
        `\n\nRelease-download error:\n  ${e.message}\n` +
        '\nFix one of:\n' +
        '  1. Build it:        cargo build --release   (creates target/release/bl)\n' +
        '  2. Point at it:     BL_BIN=/abs/path/to/bl pnpm gen:reference\n' +
        '  3. Put it on PATH:  ensure `bl` resolves\n' +
        '  4. Pin a release:   BL_VERSION=vX.Y.Z (defaults to the latest ' +
        'published release)\n',
    );
    process.exit(1);
  }
}

const BL = await resolveBin();

function blVersion() {
  try {
    return execFileSync(BL, ['--version'], { encoding: 'utf8' }).trim();
  } catch {
    return 'unknown';
  }
}

// ---------------------------------------------------------------------------
// B. CLI help parsing (Cobra + clap)
// ---------------------------------------------------------------------------

/** Run `bl <args> --help`, tolerating non-zero exit (help may exit 0/1). */
function help(args) {
  try {
    return execFileSync(BL, [...args, '--help'], {
      encoding: 'utf8',
      maxBuffer: 16 * 1024 * 1024,
    });
  } catch (e) {
    return (e.stdout || '') + (e.stderr || '');
  }
}

// Section headers that terminate the "Available Commands" block (Cobra).
const COBRA_TERMINATOR =
  /^(Flags:|Global Flags:|Examples:|Usage:|Additional Commands:|Additional help topics:)/;

/**
 * Parse the leading description block of a Cobra help page: everything before
 * the first `Usage:` line. Collapses to a one-line summary + optional details.
 */
function parseDescription(text) {
  const lines = text.split('\n');
  const usageIdx = lines.findIndex((l) => /^Usage:/.test(l));
  const head = (usageIdx >= 0 ? lines.slice(0, usageIdx) : lines.slice(0, 1))
    .join('\n')
    .trim();
  if (!head) return { summary: '', details: '' };
  const blocks = head.split(/\n\s*\n/);
  return {
    summary: blocks[0].replace(/\s+/g, ' ').trim(),
    details: blocks.slice(1).join('\n\n').trim(),
  };
}

/** Extract the `Usage:` lines (one or more). */
function parseUsage(text) {
  const lines = text.split('\n');
  const i = lines.findIndex((l) => /^Usage:/.test(l));
  if (i < 0) return [];
  // clap: single-line "Usage: bl add-mcp [OPTIONS] [client]"
  const inline = lines[i].replace(/^Usage:\s*/, '').trim();
  if (inline) return [inline];
  // Cobra: blank then indented usage lines until next blank/section
  const out = [];
  for (let j = i + 1; j < lines.length; j++) {
    const l = lines[j];
    if (l.trim() === '') break;
    out.push(l.trim());
  }
  return out;
}

/**
 * Parse "Examples:" into structured blocks. Cobra renders examples as groups
 * separated by blank lines, each a mix of command lines (`bl ...`) and comment
 * lines (`# ...` — often expected output, e.g. `# → @e1 [button] "Submit"`).
 * We keep the raw block text so the code fence preserves the worked examples.
 */
function parseExamples(text) {
  const lines = text.split('\n');
  const i = lines.findIndex((l) => /^Examples:/.test(l));
  if (i < 0) return '';
  const out = [];
  for (let j = i + 1; j < lines.length; j++) {
    const l = lines[j];
    // Examples end at the next un-indented section header.
    if (/^\S/.test(l) && l.trim() !== '') break;
    out.push(l.replace(/^ {2}/, '')); // strip the common 2-space indent
  }
  // Trim leading/trailing blank lines.
  while (out.length && out[0].trim() === '') out.shift();
  while (out.length && out[out.length - 1].trim() === '') out.pop();
  return out.join('\n');
}

/**
 * Parse a flags block (Cobra "Flags:"/"Global Flags:" or clap "Options:").
 * Returns [{ short, long, arg, desc }]. Handles wrapped continuation lines.
 */
function parseFlagBlock(text, header) {
  const lines = text.split('\n');
  const i = lines.findIndex((l) => new RegExp('^' + header).test(l));
  if (i < 0) return [];
  const flags = [];
  let cur = null;
  for (let j = i + 1; j < lines.length; j++) {
    const l = lines[j];
    if (l.trim() === '') {
      // blank line ends a contiguous flag block
      if (cur) {
        flags.push(cur);
        cur = null;
      }
      break;
    }
    if (/^\S/.test(l)) break; // next un-indented section
    // A flag line starts with optional short (-x,) then a long (--xxx).
    const m = l.match(
      /^\s+(?:(-\w),\s+)?(--[\w-]+|-\w)(?:\s+(<[^>]+>|\S+))?\s{2,}(.*)$/,
    );
    if (m) {
      if (cur) flags.push(cur);
      let short = m[1] || '';
      let long = m[2] || '';
      let arg = m[3] || '';
      // Drop the universal --help flag — it's noise on every page.
      if (long === '--help' || (long === '-h' && !short)) {
        cur = null;
        continue;
      }
      // clap sometimes lists only a long flag without a type token; Cobra puts
      // the value type (string/int/duration/...) in the `arg` slot.
      cur = { short, long, arg, desc: m[4].trim() };
    } else if (cur) {
      // continuation of previous flag description (wrapped line)
      cur.desc += ' ' + l.trim();
    }
  }
  if (cur) flags.push(cur);
  return flags;
}

/** clap "Arguments:" block → [{ name, desc }]. */
function parseClapArguments(text) {
  const lines = text.split('\n');
  const i = lines.findIndex((l) => /^Arguments:/.test(l));
  if (i < 0) return [];
  const out = [];
  let cur = null;
  for (let j = i + 1; j < lines.length; j++) {
    const l = lines[j];
    if (l.trim() === '') {
      if (cur) out.push(cur);
      break;
    }
    if (/^\S/.test(l)) break;
    const m = l.match(/^\s+(\[?<?[\w.-]+>?\]?(?:\.\.\.)?)\s{2,}(.*)$/);
    if (m) {
      if (cur) out.push(cur);
      cur = { name: m[1], desc: m[2].trim() };
    } else if (cur) {
      cur.desc += ' ' + l.trim();
    }
  }
  if (cur && !out.includes(cur)) out.push(cur);
  return out;
}

/**
 * Parse "Available Commands". Top-level help groups commands under category
 * headers (un-indented `Name:` lines); subcommand help lists them flat. We
 * return both the flat command names and (for top-level) the categories.
 */
function parseAvailableCommands(text) {
  const lines = text.split('\n');
  const i = lines.findIndex((l) => /^Available Commands:/.test(l));
  if (i < 0) return { categories: [], commands: [] };
  const categories = [];
  let cur = { name: null, commands: [] };
  categories.push(cur);
  for (let j = i + 1; j < lines.length; j++) {
    const l = lines[j];
    if (l.trim() === '') continue;
    if (COBRA_TERMINATOR.test(l) || /^Use "/.test(l)) break;
    // category header: un-indented, ends with ":"
    if (/^\S.*:\s*$/.test(l)) {
      cur = { name: l.trim().replace(/:$/, ''), commands: [] };
      categories.push(cur);
      continue;
    }
    const m = l.match(/^\s{2,}(\S+)\s+(.*)$/);
    if (m && m[1] !== 'help') {
      cur.commands.push({ name: m[1], summary: m[2].trim() });
    }
  }
  const filtered = categories.filter((c) => c.commands.length > 0);
  const commands = filtered.flatMap((c) => c.commands.map((x) => x.name));
  return { categories: filtered, commands };
}

/**
 * Recursively build the CLI command tree. Each node:
 *   { path: ['find','role'], help, description, usage, examples,
 *     localFlags, globalFlags, clapArgs, isClap, children: [node...] }
 */
function buildCliNode(path) {
  const text = help(path);
  const isClap = /^Usage:\s+\S/m.test(text) && /^(Options|Arguments):/m.test(text);
  const { categories, commands } = parseAvailableCommands(text);
  const node = {
    path,
    name: path[path.length - 1],
    full: 'bl ' + path.join(' '),
    help: text,
    isClap,
    ...parseDescription(text),
    usage: parseUsage(text),
    examples: parseExamples(text),
    localFlags: parseFlagBlock(text, isClap ? 'Options:' : 'Flags:'),
    globalFlags: isClap ? [] : parseFlagBlock(text, 'Global Flags:'),
    clapArgs: isClap ? parseClapArguments(text) : [],
    children: [],
  };
  for (const c of commands) {
    node.children.push(buildCliNode([...path, c]));
  }
  return node;
}

/** Walk the top-level help to get categories + their command nodes. */
function buildCliTree() {
  const top = help([]);
  const { categories } = parseAvailableCommands(top);
  return categories.map((cat) => ({
    name: cat.name,
    commands: cat.commands.map((c) => buildCliNode([c.name])),
  }));
}

// ---------------------------------------------------------------------------
// C. MCP handshake (stdio JSON-RPC 2.0 → tools/list)
// ---------------------------------------------------------------------------

function fetchMcpTools() {
  return new Promise((resolvePromise, reject) => {
    const child = spawn(BL, ['mcp'], {
      stdio: ['pipe', 'pipe', 'ignore'],
    });
    let buf = '';
    let done = false;
    const timer = setTimeout(() => {
      if (!done) {
        done = true;
        try {
          child.kill('SIGKILL');
        } catch {}
        reject(new Error('MCP tools/list timed out after 20s'));
      }
    }, 20000);

    child.stdout.setEncoding('utf8');
    child.stdout.on('data', (chunk) => {
      buf += chunk;
      let nl;
      while ((nl = buf.indexOf('\n')) >= 0) {
        const line = buf.slice(0, nl);
        buf = buf.slice(nl + 1);
        if (!line.trim()) continue;
        let msg;
        try {
          msg = JSON.parse(line);
        } catch {
          continue;
        }
        if (msg.id === 2 && msg.result && Array.isArray(msg.result.tools)) {
          done = true;
          clearTimeout(timer);
          try {
            child.kill('SIGTERM');
          } catch {}
          resolvePromise(msg.result.tools);
          return;
        }
      }
    });
    child.on('error', (err) => {
      if (!done) {
        done = true;
        clearTimeout(timer);
        reject(err);
      }
    });
    child.on('close', () => {
      if (!done) {
        done = true;
        clearTimeout(timer);
        reject(new Error('MCP server exited before returning tools/list'));
      }
    });

    const send = (obj) => child.stdin.write(JSON.stringify(obj) + '\n');
    send({
      jsonrpc: '2.0',
      id: 1,
      method: 'initialize',
      params: {
        protocolVersion: '2024-11-05',
        capabilities: {},
        clientInfo: { name: 'gen', version: '0' },
      },
    });
    send({ jsonrpc: '2.0', method: 'notifications/initialized' });
    send({ jsonrpc: '2.0', id: 2, method: 'tools/list', params: {} });
  });
}

// ---------------------------------------------------------------------------
// MDX helpers
// ---------------------------------------------------------------------------

/**
 * Make freeform text safe to drop into MDX *prose* (not code fences). MDX is
 * JSX-aware, so a literal `<select>` is parsed as a JSX tag and `{ ... }` as a
 * JS expression. We convert those to HTML entities, which render as the literal
 * characters. Apply to summaries/descriptions and (via `td`) table cells.
 */
function mdxInline(s) {
  if (s === undefined || s === null) return '';
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/\{/g, '&#123;')
    .replace(/\}/g, '&#125;');
}

/** Escape a value for safe display inside a markdown table cell. */
function td(s) {
  if (s === undefined || s === null) return '';
  return mdxInline(String(s).replace(/\r?\n/g, ' ').trim()).replace(
    /\|/g,
    '\\|',
  );
}

/**
 * Render a multi-line freeform description block (Cobra long descriptions, e.g.
 * the `completion <shell>` setup instructions) as a verbatim fenced code block.
 * Fenced code is immune to MDX/JSX parsing, so `<(...)`, `${...}`, `#### …` and
 * tab-indented commands survive exactly as the binary prints them.
 */
function detailsBlock(s) {
  return '```text\n' + String(s).replace(/```/g, '``​`') + '\n```';
}

/** Escape frontmatter string values (YAML double-quoted). */
function yaml(s) {
  return '"' + String(s).replace(/\\/g, '\\\\').replace(/"/g, '\\"') + '"';
}

/**
 * Frontmatter `description` values flow from the binary's help text (which can
 * contain `<select>`, `{...}`, etc.) into a YAML string that Fumadocs renders
 * into `<meta name="description">` / OpenGraph tags. A raw `<select>` there is
 * an unbalanced JSX-looking tag. HTML-escape the JSX-significant characters
 * (same scheme as `mdxInline`) BEFORE YAML-quoting so the description carries
 * harmless entities rather than a raw `<…>`/`{…}`.
 */
function yamlDescription(s) {
  return yaml(mdxInline(s));
}

/** Render a flag's invocation token, e.g. `-o, --output string`. */
function flagToken(f) {
  let t = '';
  if (f.short) t += `${f.short}, `;
  t += f.long;
  if (f.arg) t += ` ${f.arg}`;
  return t;
}

function flagsTable(flags) {
  const rows = flags.map((f) => `| \`${flagToken(f)}\` | ${td(f.desc)} |`);
  return ['| Flag | Description |', '| --- | --- |', ...rows].join('\n');
}

function writeMdx(file, frontmatter, bodyParts) {
  mkdirSync(dirname(file), { recursive: true });
  const fm = ['---', ...frontmatter, '---'].join('\n');
  const body = bodyParts.filter((p) => p != null && p !== '').join('\n\n');
  writeFileSync(file, `${fm}\n\n${AUTOGEN_HEADER}\n\n${body}\n`);
}

function writeMeta(dir, obj) {
  mkdirSync(dir, { recursive: true });
  writeFileSync(join(dir, 'meta.json'), JSON.stringify(obj, null, 2) + '\n');
}

/** slugify a command path: ['find','role'] -> 'find-role'. */
function cmdSlug(path) {
  return path.join('-');
}

