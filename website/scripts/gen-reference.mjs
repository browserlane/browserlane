/** Is a command path under one of the curated CLI-only roots? */
function isCliOnlyRoot(path) {
  return CLI_ONLY_ROOTS.has(path[0]);
}

/**
 * The 12 MCP tools the spec marks as MCP-only: all page_clock_* (8) plus the
 * record group/chunk lifecycle (4). We assert the derived set matches this.
 */
