function jsType(schemaProp) {
  if (!schemaProp) return '';
  if (Array.isArray(schemaProp.type)) return schemaProp.type.join(' | ');
  return schemaProp.type || (schemaProp.enum ? 'enum' : '');
}

function renderToolBody(tool, depth) {
  const parts = [];
  const h = '#'.repeat(depth);
  parts.push(`${h} \`${tool.name}\``);
  if (tool.description) parts.push(mdxInline(tool.description));
  const schema = tool.inputSchema || {};
  const props = schema.properties || {};
  const required = new Set(schema.required || []);
  const keys = Object.keys(props);
  if (keys.length) {
    const rows = keys.map((k) => {
      const p = props[k] || {};
      const def = p.default !== undefined ? '`' + JSON.stringify(p.default) + '`' : '';
      const enumNote =
        Array.isArray(p.enum) && p.enum.length
          ? ` (one of: ${p.enum.map((v) => '`' + v + '`').join(', ')})`
          : '';
      return `| \`${td(k)}\` | \`${td(jsType(p))}\` | ${required.has(k) ? 'yes' : 'no'} | ${def} | ${td((p.description || '') + enumNote)} |`;
    });
    parts.push(
      [
        '| Parameter | Type | Required | Default | Description |',
        '| --- | --- | --- | --- | --- |',
        ...rows,
      ].join('\n'),
    );
  } else {
    parts.push('_No parameters._');
  }
  return parts.join('\n\n');
}

function emitMcp(tools, version) {
  rmSync(MCP_DIR, { recursive: true, force: true });
  mkdirSync(MCP_DIR, { recursive: true });

  const byGroup = new Map(MCP_GROUPS.map((g) => [g.slug, []]));
  const ungrouped = [];
  for (const t of tools) {
    const g = mcpGroupOf(t.name);
    if (g) byGroup.get(g.slug).push(t);
    else ungrouped.push(t);
  }
  if (ungrouped.length) {
    console.warn(
      `[gen-reference] ${ungrouped.length} MCP tools fell through grouping: ` +
        ungrouped.map((t) => t.name).join(', '),
    );
    byGroup.set('other', ungrouped);
  }

  // One page per group; tools sorted alphabetically within a group.
  const pageSlugs = ['index'];
  for (const g of MCP_GROUPS) {
    const list = (byGroup.get(g.slug) || []).slice().sort((a, b) =>
      a.name.localeCompare(b.name),
    );
    if (!list.length) continue;
    const body = [
      `${list.length} tool${list.length === 1 ? '' : 's'} in this group.`,
      ...list.map((t) => renderToolBody(t, 2)),
    ].join('\n\n');
    writeMdx(
      join(MCP_DIR, `${g.slug}.mdx`),
      [`title: ${yaml(g.title)}`, `description: ${yaml(g.title + ' — MCP tools exposed to agents.')}`],
      [body],
    );
    pageSlugs.push(g.slug);
  }
  if (byGroup.get('other') && byGroup.get('other').length) {
    const list = byGroup.get('other').slice().sort((a, b) => a.name.localeCompare(b.name));
    const body = list.map((t) => renderToolBody(t, 2)).join('\n\n');
    writeMdx(
      join(MCP_DIR, 'other.mdx'),
      ['title: Other', 'description: "Uncategorized MCP tools."'],
      [body],
    );
    pageSlugs.push('other');
  }

  // Landing index with a full tool inventory table.
  const indexBody = [];
  indexBody.push(
    `Every MCP tool browserlane exposes to agents over stdio JSON-RPC — ` +
      `generated from \`${version}\`. **${tools.length} tools** total. ` +
      `Start the server with \`bl mcp\`.`,
  );
  indexBody.push(
    `See the [CLI ↔ MCP mapping](/cli-reference/cli-mcp-mapping) for how the ` +
      `tools relate to the \`bl\` CLI (the two surfaces are not 1:1).`,
  );
  for (const g of MCP_GROUPS) {
    const list = (byGroup.get(g.slug) || []).slice().sort((a, b) =>
      a.name.localeCompare(b.name),
    );
    if (!list.length) continue;
    const rows = list.map(
      (t) => `| \`${t.name}\` | ${td(t.description)} |`,
    );
    indexBody.push(
      `## ${g.title}\n\n| Tool | Description |\n| --- | --- |\n${rows.join('\n')}`,
    );
  }
  writeMdx(
    join(MCP_DIR, 'index.mdx'),
    ['title: MCP reference', `description: ${yaml('Complete reference for every MCP tool browserlane exposes to agents.')}`],
    [indexBody.join('\n\n')],
  );

  // root: true makes this folder a top-level tab in the docs layout.
  writeMeta(MCP_DIR, { title: 'MCP reference', root: true, pages: pageSlugs });

  return { byGroup, ungrouped };
}

