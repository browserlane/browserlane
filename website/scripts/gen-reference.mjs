/**
 * Split the leading YAML frontmatter off the raw `SKILL.md`. Returns
 * `{ description, body }` — `body` is the markdown after the frontmatter with
 * its top-level H1 removed (the page renders its title from frontmatter, so a
 * body H1 would duplicate it).
 */
function parseSkill(raw) {
  let description = 'The browserlane agent skill.';
  let body = raw;
  const fm = raw.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
  if (fm) {
    body = raw.slice(fm[0].length);
    const m = fm[1].match(/^description:\s*(.+)$/m);
    if (m) description = m[1].trim().replace(/^["']|["']$/g, '');
  }
  // Drop a single leading H1 (and surrounding blank lines).
  body = body.replace(/^\s*#\s+.*\r?\n+/, '');
  return { description, body: body.trim() };
}

