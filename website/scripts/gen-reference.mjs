/** Render one command node (and its subcommands) into a single MDX body. */
function renderCliBody(node, depth = 2) {
  const parts = [];
  const h = '#'.repeat(Math.min(depth, 6));
  // For the top-level command on its own page we rely on frontmatter title,
  // so the first heading is omitted; subcommands get their own heading.
  if (depth > 2) {
    parts.push(`${h} \`${node.full}\``);
  }
  if (node.summary) parts.push(mdxInline(node.summary));
  if (node.details) parts.push(detailsBlock(node.details));

  if (node.usage.length) {
    parts.push(
      `**Usage**\n\n\`\`\`bash\n${node.usage.join('\n')}\n\`\`\``,
    );
  }

  if (node.clapArgs.length) {
    const rows = node.clapArgs.map(
      (a) => `| \`${td(a.name)}\` | ${td(a.desc)} |`,
    );
    parts.push(
      ['**Arguments**', '', '| Argument | Description |', '| --- | --- |', ...rows].join('\n'),
    );
  }

  // De-duplicate global flags that also appear locally (by long name).
  const localLongs = new Set(node.localFlags.map((f) => f.long));
  const globals = node.globalFlags.filter((f) => !localLongs.has(f.long));
  if (node.localFlags.length) {
    parts.push('**Flags**\n\n' + flagsTable(node.localFlags));
  }
  if (globals.length) {
    parts.push('**Global flags**\n\n' + flagsTable(globals));
  }

  if (node.examples) {
    parts.push('**Examples**\n\n```bash\n' + node.examples + '\n```');
  }

  for (const child of node.children) {
    parts.push(renderCliBody(child, depth + 1));
  }
  return parts.join('\n\n');
}

function emitCli(tree, version) {
  // wipe + recreate
  rmSync(CLI_DIR, { recursive: true, force: true });
  mkdirSync(CLI_DIR, { recursive: true });

  // Index / landing page (also the mapping intro lives elsewhere).
  const totalCommands = countNodes(tree);

  // Bucket commands by group slug, pulling advanced ones out.
  const groupPages = new Map(); // slug -> [{slug,file,title}]
  const advancedPages = [];

  for (const group of tree) {
    const slug = CLI_GROUP_SLUG.get(group.name);
    if (!slug) {
      console.warn(`[gen-reference] unknown CLI group "${group.name}"`);
    }
    for (const node of group.commands) {
      const isAdvanced = ADVANCED_COMMANDS.has(node.name);
      const targetSlug = isAdvanced ? ADVANCED_SLUG : slug || 'misc';
      const dir = join(CLI_DIR, targetSlug);
      const pageSlug = cmdSlug(node.path);
      const file = join(dir, `${pageSlug}.mdx`);
      const title = node.full;
      const desc = node.summary || `Reference for ${node.full}.`;
      writeMdx(
        file,
        [`title: ${yaml(title)}`, `description: ${yamlDescription(desc)}`],
        [renderCliBody(node, 2)],
      );
      const entry = { pageSlug, title, summary: node.summary };
      if (isAdvanced) advancedPages.push(entry);
      else {
        if (!groupPages.has(targetSlug)) groupPages.set(targetSlug, []);
        groupPages.get(targetSlug).push(entry);
      }
    }
  }

  // Per-group meta.json (preserve discovery order from the binary). A folder
  // title can be overridden (e.g. "Setup & diagnostics" -> "Setup").
  const groupFolderTitles = new Map(
    CLI_GROUP_ORDER.map(([name, s]) => [s, CLI_GROUP_FOLDER_TITLE.get(s) || name]),
  );
  for (const [slug, pages] of groupPages) {
    writeMeta(join(CLI_DIR, slug), {
      title: groupFolderTitles.get(slug) || slug,
      pages: pages.map((p) => p.pageSlug),
    });
  }
  // Advanced group.
  if (advancedPages.length) {
    writeMeta(join(CLI_DIR, ADVANCED_SLUG), {
      title: 'Advanced / diagnostics',
      pages: advancedPages.map((p) => p.pageSlug),
    });
  }

  // Landing index.mdx with an at-a-glance table of every command.
  const indexBody = [];
  indexBody.push(
    `The full reference for every \`bl\` command, argument, and flag — ` +
      `generated from \`${version}\`. ` +
      `${totalCommands} commands across ${tree.length} groups.`,
  );
  indexBody.push(
    `See the [CLI ↔ MCP mapping](/cli-reference/cli-mcp-mapping) for how ` +
      `each command lines up with the agent-facing MCP tools.`,
  );
  for (const group of tree) {
    const rows = [];
    const walkRows = (node) => {
      rows.push(`| \`${node.full}\` | ${td(node.summary)} |`);
      node.children.forEach(walkRows);
    };
    const nonAdvanced = group.commands.filter(
      (c) => !ADVANCED_COMMANDS.has(c.name),
    );
    nonAdvanced.forEach(walkRows);
    if (!rows.length) continue;
    indexBody.push(
      `## ${group.name}\n\n| Command | Description |\n| --- | --- |\n${rows.join('\n')}`,
    );
  }
  // Advanced section in index.
  {
    const rows = [];
    for (const group of tree) {
      for (const node of group.commands) {
        if (ADVANCED_COMMANDS.has(node.name)) {
          const walk = (n) => {
            rows.push(`| \`${n.full}\` | ${td(n.summary)} |`);
            n.children.forEach(walk);
          };
          walk(node);
        }
      }
    }
    if (rows.length) {
      indexBody.push(
        `## Advanced / diagnostics\n\nLow-level probes for debugging the ` +
          `browser/WebDriver stack — not part of the everyday workflow.\n\n` +
          `| Command | Description |\n| --- | --- |\n${rows.join('\n')}`,
      );
    }
  }
  writeMdx(
    join(CLI_DIR, 'index.mdx'),
    ['title: CLI reference', `description: ${yaml('Complete reference for every browserlane CLI command and flag.')}`],
    [indexBody.join('\n\n')],
  );

  // Mapping page is emitted separately (emitMapping) into this same dir.

  // Top-level meta.json: index, mapping, then groups in order, Advanced last.
  const orderedGroupSlugs = CLI_GROUP_ORDER.map(([, s]) => s).filter((s) =>
    groupPages.has(s),
  );
  const pages = ['index', 'cli-mcp-mapping', ...orderedGroupSlugs];
  if (advancedPages.length) pages.push(ADVANCED_SLUG);
  writeMeta(CLI_DIR, { title: 'CLI reference', pages });

  return { totalCommands };
}

function countNodes(tree) {
  let n = 0;
  const walk = (node) => {
    n += 1;
    node.children.forEach(walk);
  };
  tree.forEach((g) => g.commands.forEach(walk));
  return n;
}

/** Flat list of every CLI command node (for mapping + counts). */
function flattenCli(tree) {
  const out = [];
  const walk = (node) => {
    out.push(node);
    node.children.forEach(walk);
  };
  tree.forEach((g) => g.commands.forEach(walk));
  return out;
}

